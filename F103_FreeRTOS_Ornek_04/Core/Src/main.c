int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USART1_UART_Init();

  HAL_UART_Transmit(&huart1, (uint8_t *)"UART BASLADI\r\n", 14, 0xFFFF);

  static const char *Gorev1Metin = "Gorev 1 calisiyor\r\n";
  static const char *Gorev2Metin = "Gorev 2 calisiyor\r\n";

  xTaskCreate(vTask1, "Task 1", configMINIMAL_STACK_SIZE,  (void*)Gorev1Metin, 1, NULL);
  xTaskCreate(vTask2, "Task 2", configMINIMAL_STACK_SIZE,  (void*)Gorev2Metin, 2, NULL);
  vTaskStartScheduler();

  while (1);
}

void vTask1( void *pvParameters )
{
	char *GorevAdi;
	GorevAdi = ( char * ) pvParameters;
	const TickType_t xDelay250ms = pdMS_TO_TICKS(250);
	for( ;; )
	{
		HAL_UART_Transmit(&huart1, (uint8_t *)GorevAdi, 19, 0xFFFF);
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
        /* Biraz Gecikme*/
		vTaskDelay(xDelay250ms);
	}
}

